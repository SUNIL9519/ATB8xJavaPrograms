package sept.ex_25092024_Loop;

public class Lab091 {
    public static void main(String[] args) {
        // While loop
        // while -> A; B;C
                  // A -> Initialization
                  // B -> Condition
                  // C -> Increment/ Decrement
        int i = 0; // A
        while(i <= 10) { // B
            System.out.println(i);
            i++;
        }
        for(int j = 0; j <= 10; j++) { // for loop
            System.out.println(j);


        }

    }
}
