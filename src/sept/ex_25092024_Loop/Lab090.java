package sept.ex_25092024_Loop;

public class Lab090 {
    public static void main(String[] args) {
        for (int i = 0; i < 10; ++i){
            System.out.println(i);
        }
        System.out.println(" __ ");
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
        System.out.println(" __ ");
        for (char c = 'A'; c <= 'Z'; c++) {
            System.out.println(c);
        }

    }
}
