package sept.ex_23092024;

public class Lab071 {
    public static void main(String[] args) {
        // Interview Question
        // char ch = 'a' -> vowel/ consonat
        // vowel -> a, e, i, o ,u
        char ch = 'a';
        if (ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u') {
            System.out.println("Vowel");
        }else{
            System.out.println("consonat");
        }
    }
}
