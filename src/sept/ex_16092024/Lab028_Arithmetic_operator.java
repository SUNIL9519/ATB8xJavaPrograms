package sept.ex_16092024;

public class Lab028_Arithmetic_operator {
    public static void main(String[] args) {

        int a = 10;
        int b =  2;
        System.out.println(a+b); // + -> binary - 2 operands(a,b)
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        // % is called as modulus which gives remainders

        System.out.println(a%b);
    }
}
