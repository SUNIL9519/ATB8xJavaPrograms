package sept.ex_16092024;

public class Lab037 {
    public static void main(String[] args) {
        // ! -> logical NOT
        boolean a = true;
        System.out.println(!a); // false
        System.out.println(!(10>20)); // true
        System.out.println(!!!!(30>90)); // false
        // System.out.println((10>20)!); can not use ! at the end
    }
}
